public class TypeCasting3 {
    public static void main(String[] args) {
        float value1 = 3;
        long value2 = 5;
        double value3 = 7;
        byte value4 = 9;
        short value5 = 11;

        System.out.println(value1);
        System.out.println(value2);
        System.out.println(value3);
        System.out.println(value4);
        System.out.println(value5);
    }
}